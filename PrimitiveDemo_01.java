package in.datatypes;

public class PrimitiveDemo_01 {

	public static void main(String[] args) {
		 byte a = 10;
	     short b = 200;
	     int c = 5000;
	     long d = 999999L;
	     float e = 2.5f;
	     double f = 5.678;
	     char g = 'A';
	     boolean h = true;

	     System.out.println(a+" "+b+" "+c+" "+d+" "+e+" "+f+" "+g+" "+h);
	}

}
